#ifndef CTL_CODE
#define FILE_ANY_ACCESS 0
#define METHOD_BUFFERED 0
#define METHOD_NEITHER 3
#define FILE_DEVICE_UNKNOWN 0x22
#define CTL_CODE( DeviceType, Function, Method, Access ) \
    (((DeviceType) << 16) | ((Access) << 14) | ((Function) << 2) | (Method))
#endif

